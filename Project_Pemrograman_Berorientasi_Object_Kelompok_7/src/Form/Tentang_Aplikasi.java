    // Method Send
    private void Send()
    {
        nama = tfnama.getText();
        masukkan = tfmasukkan.getText();
        nilai = null;
        ps = null;
        sql = null;
        sql = "Insert into tbl_ulasan values(?,?,?,?,?)";
        try
        {
            if(btn_grup_penilaian.equals(""))
            {
                JOptionPane.showMessageDialog(this, "Maaf, penilaian belum dipilih üôèüèª ", "Perhatian", JOptionPane.WARNING_MESSAGE);
                rb5.requestFocus();
            }
            else if(nama.equals(""))
            {
                JOptionPane.showMessageDialog(this, "Maaf, kolom nama belum terisi üôèüèª ", "Perhatian", JOptionPane.WARNING_MESSAGE);
                tfnama.requestFocus();
            }
            else if(masukkan.equals(""))
            {
                JOptionPane.showMessageDialog(this, "Maaf, kolom pendapat, saran atau kritik belum diisi üôèüèª ", "Perhatian", JOptionPane.WARNING_MESSAGE);
                tfmasukkan.requestFocus();
            }
            else 
            {
                 ps = Koneksikedatabase.ambilKoneksiDatabase().prepareStatement(sql);
                 if(rb1.isSelected() == true)
                 {
                     nilai = "1";
                 }
                 else if(rb2.isSelected() == true)
                 {
                     nilai = "2";
                 }
                 else if(rb3.isSelected() == true)
                 {
                     nilai = "3";
                 }
                 else if(rb4.isSelected() == true)
                 {
                     nilai = "4";
                 }
                 else if(rb5.isSelected() == true)
                 {
                     nilai = "5";
                 }
                 ps.setString(1, null);
                 ps.setString(2, nilai);
                 ps.setString(3, nama);
                 ps.setString(4, masukkan);
                 ps.setString(5, null);
                 ps.executeUpdate();
                 JOptionPane.showMessageDialog(this, "Terima kasih, masukkan anda telah dikirim üôèüèª ", "Informasi", JOptionPane.INFORMATION_MESSAGE);
                 KosongkanData();
                 tfnama.requestFocus();
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, "Maaf, masukkan anda gagal dikirim " + e.getMessage(), "Pemberitahuan", JOptionPane.ERROR_MESSAGE);
            tfnama.requestFocus();
        }
    }
      
